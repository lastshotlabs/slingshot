async function redisGetDel(redis: RedisLike, key: string): Promise<string | null> {
  if (typeof redis.getdel === 'function') {
    try {
      return await redis.getdel(key);
    } catch (err: unknown) {
      const msg = err instanceof Error ? err.message : '';
      if (!/unknown command|ERR unknown command/i.test(msg)) throw err;
    }
  }
  const result = await redis.eval(
    "local v = redis.call('GET', KEYS[1])\nif v then redis.call('DEL', KEYS[1]) end\nreturn v",
    1,
    key,
  );
  return (result as string | null) ?? null;
}

export function createRedisOAuthStateStore(
  getRedis: () => RedisLike,
  appName: string,
): OAuthStateStore {
  return {
    async store(state, codeVerifier, linkUserId) {
      await getRedis().set(
        `oauth:${appName}:state:${state}`,
        JSON.stringify({ codeVerifier, linkUserId }),
        'EX',
        STATE_TTL,
      );
    },
    async consume(state) {
      const key = `oauth:${appName}:state:${state}`;
      const value = await redisGetDel(getRedis(), key);
      if (!value) return null;
      return JSON.parse(value) as OAuthStateRecord;
    },
  };
}

