// SQLite repository factory
// ---------------------------------------------------------------------------

export function createSqliteOAuthStateStore(db: RuntimeSqliteDatabase): OAuthStateStore {
  const init = createSqliteInitializer(db, () => {
    db.run(`CREATE TABLE IF NOT EXISTS oauth_states (
      state      TEXT PRIMARY KEY,
      codeVerifier TEXT,
      linkUserId TEXT,
      expiresAt  INTEGER NOT NULL
    )`);
    db.run('CREATE INDEX IF NOT EXISTS idx_oauth_states_expiresAt ON oauth_states(expiresAt)');
  });

  return {
    async store(state, codeVerifier, linkUserId) {
      init();
      const expiresAt = Date.now() + STATE_TTL * 1000;
      db.run(
        `INSERT INTO oauth_states (state, codeVerifier, linkUserId, expiresAt)
         VALUES (?, ?, ?, ?)
         ON CONFLICT(state) DO UPDATE SET codeVerifier = excluded.codeVerifier, linkUserId = excluded.linkUserId, expiresAt = excluded.expiresAt`,
        state,
        codeVerifier ?? null,
        linkUserId ?? null,
        expiresAt,
      );
    },
    async consume(state) {
      init();
      const now = Date.now();
      const row = db
        .query(
          'DELETE FROM oauth_states WHERE state = ? AND expiresAt > ? RETURNING codeVerifier, linkUserId',
        )
        .get(state, now) as { codeVerifier?: string; linkUserId?: string } | null;
      return row ?? null;
    },
  };
}

