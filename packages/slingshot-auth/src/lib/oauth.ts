/**
 * Union of all provider keys for which credentials were configured.
 * Derived from the keys of `OAuthProviders`.
 */
export type ConfiguredOAuthProvider = keyof OAuthProviders;

/**
 * The data payload associated with an OAuth state value.
 *
 * Stored alongside the state string in `OAuthStateStore`. Retrieved atomically by
 * `consume` when the callback arrives with the matching state parameter.
 *
 * - `codeVerifier` — the PKCE code verifier for providers that require it (Google, etc.).
 * - `linkUserId` — the ID of an already-authenticated user who initiated a "link provider" flow.
 */
export interface OAuthStateRecord {
  codeVerifier?: string;
  linkUserId?: string;
}

/**
 * Storage contract for OAuth PKCE state values.
 *
 * The `state` parameter is a cryptographically random string passed through the OAuth
 * redirect and validated on callback to prevent CSRF. When PKCE is used (e.g. Google),
 * the `codeVerifier` is stored alongside the state. When linking a provider to an
 * existing account, `linkUserId` records which user initiated the flow.
 *
 * State entries expire after 300 seconds (5 minutes), matching the typical OAuth
 * redirect timeout.
 */
export interface OAuthStateStore {
  /**
   * Stores an OAuth state entry.
   *
   * @param state - The cryptographically random state string generated by `generateState()`.
   *   This is the key for later retrieval on callback.
   * @param codeVerifier - Optional PKCE code verifier generated by `generateCodeVerifier()`.
   *   Required for providers that enforce PKCE (e.g. Google). Pass `undefined` for providers
   *   that do not use PKCE.
   * @param linkUserId - Optional ID of an already-authenticated user who initiated a
   *   "link provider" flow. When present, the callback route will link the OAuth identity
   *   to this user rather than creating a new account or logging in.
   * @returns `Promise<void>` — resolves when the state has been durably stored.
   *
   * @remarks
   * State entries have a fixed TTL of 300 seconds (5 minutes). If the user does not
   * complete the OAuth redirect within that window, `consume` will return `null` and
   * the login or link flow must be restarted.
   */
  store(state: string, codeVerifier?: string, linkUserId?: string): Promise<void>;

  /**
   * Atomically reads and deletes the state entry identified by `state`.
   *
   * @param state - The state string from the OAuth callback `?state=` query parameter.
   * @returns The stored `OAuthStateRecord` (`{ codeVerifier?, linkUserId? }`) on success,
   *   or `null` when the state is not found, has expired, or was already consumed.
   *
   * @remarks
   * **Single-use atomicity**: all backends implement this as an atomic read-and-delete
   * (Redis `GETDEL` / Lua fallback, SQLite `DELETE ... RETURNING`, Postgres
   * `DELETE ... RETURNING`, MongoDB `findOneAndDelete`). Once consumed a state value
   * is permanently gone — a second call with the same state string returns `null`.
   * This prevents OAuth callback replay attacks where an attacker re-submits a valid
   * callback URL.
   */
  consume(state: string): Promise<OAuthStateRecord | null>;
}

const STATE_TTL = 300;

// ---------------------------------------------------------------------------
// Memory repository factory
// ---------------------------------------------------------------------------

interface MemoryOAuthStateEntry {
  codeVerifier?: string;
  linkUserId?: string;
  expiresAt: number;
}

/**
 * Creates an in-memory OAuth state store.
 *
 * Entries expire after 300 seconds (matching the OAuth state TTL). Eviction is
 * opportunistic — expired entries are swept on `store` calls. Suitable for
 * development and single-server deployments.
 *
 * @returns An `OAuthStateStore` backed by in-memory Maps.
 *
 * @example
 * // In tests
 * const stateStore = createMemoryOAuthStateStore();
 * await stateStore.store('random-state-value', 'code-verifier');
 * const record = await stateStore.consume('random-state-value');
 * // record: { codeVerifier: 'code-verifier' }
 */
export function createMemoryOAuthStateStore(): OAuthStateStore {
  const states = new Map<string, MemoryOAuthStateEntry>();
  const evictExpired = createEvictExpired();

  return {
    async store(state, codeVerifier, linkUserId) {
      evictExpired(states);
      evictOldest(states, DEFAULT_MAX_ENTRIES);
      states.set(state, { codeVerifier, linkUserId, expiresAt: Date.now() + STATE_TTL * 1000 });
    },
    async consume(state) {
      const entry = states.get(state);
      if (!entry || entry.expiresAt <= Date.now()) {
        states.delete(state);
        return null;
      }
      states.delete(state);
      return { codeVerifier: entry.codeVerifier, linkUserId: entry.linkUserId };
    },
  };
}

