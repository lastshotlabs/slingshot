export function createPostgresOAuthStateStore(pool: import('pg').Pool): OAuthStateStore {
  const ensureTable = createPostgresInitializer(pool, async client => {
    await client.query(`CREATE TABLE IF NOT EXISTS auth_oauth_state (
      state         TEXT PRIMARY KEY,
      code_verifier TEXT,
      link_user_id  TEXT,
      expires_at    BIGINT NOT NULL
    )`);
    await client.query(
      'CREATE INDEX IF NOT EXISTS idx_auth_oauth_state_expires_at ON auth_oauth_state(expires_at)',
    );
  });

  return {
    async store(state, codeVerifier, linkUserId) {
      await ensureTable();
      const expiresAt = Date.now() + STATE_TTL * 1000;
      await pool.query(
        `INSERT INTO auth_oauth_state (state, code_verifier, link_user_id, expires_at)
         VALUES ($1, $2, $3, $4)
         ON CONFLICT (state) DO UPDATE SET
           code_verifier = EXCLUDED.code_verifier,
           link_user_id  = EXCLUDED.link_user_id,
           expires_at    = EXCLUDED.expires_at`,
        [state, codeVerifier ?? null, linkUserId ?? null, expiresAt],
      );
    },
    async consume(state) {
      await ensureTable();
      const now = Date.now();
      const { rows } = await pool.query<{
        code_verifier: string | null;
        link_user_id: string | null;
      }>(
        `DELETE FROM auth_oauth_state
         WHERE state = $1 AND expires_at > $2
         RETURNING code_verifier, link_user_id`,
        [state, now],
      );
      if (!rows[0]) return null;
      return {
        codeVerifier: rows[0].code_verifier ?? undefined,
        linkUserId: rows[0].link_user_id ?? undefined,
      };
    },
  };
}

export const oauthStateFactories: RepoFactories<OAuthStateStore> = {
  memory: () => createMemoryOAuthStateStore(),
  sqlite: infra => createSqliteOAuthStateStore(infra.getSqliteDb()),
  redis: infra => createRedisOAuthStateStore(infra.getRedis, infra.appName),
  mongo: infra => {
    const { conn, mg } = infra.getMongo();
    return createMongoOAuthStateStore(conn, mg);
  },
  postgres: infra => createPostgresOAuthStateStore(infra.getPostgres().pool),
};

/**
 * Generates a cryptographically random OAuth state parameter for CSRF protection.
 *
 * The state string should be stored in `OAuthStateStore.store()` before redirecting
 * the user to the provider's authorization URL. On callback, verify it with
 * `OAuthStateStore.consume()`. Re-exported from the `arctic` library.
 *
 * @returns A random base64url-encoded state string.
 *
 * @example
 * import { generateState } from '@lastshotlabs/slingshot-auth';
 *
 * const state = generateState();
 * await runtime.oauth.stateStore.store(state, codeVerifier);
 * return c.redirect(provider.createAuthorizationURL(state, codeVerifier, scopes));
 */
export { generateState };

/**
 * Generates a cryptographically random PKCE code verifier.
 *
 * Required for OAuth providers that support PKCE (e.g. Google, GitHub). Store the
 * verifier alongside the state in `OAuthStateStore.store()` before the redirect, then
 * pass it to the token exchange call on callback. Re-exported from the `arctic` library.
 *
 * @returns A random base64url-encoded code verifier string (43–128 characters).
 *
 * @example
 * import { generateCodeVerifier, generateState } from '@lastshotlabs/slingshot-auth';
 *
 * const codeVerifier = generateCodeVerifier();
 * const state = generateState();
 * await runtime.oauth.stateStore.store(state, codeVerifier);
 */
export { generateCodeVerifier };
